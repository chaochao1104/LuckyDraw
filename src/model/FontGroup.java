package model;

import java.awt.Color;
import java.awt.Font;

public class FontGroup {
	
	private Font font;
	
	private Color color;

	public Font getFont() {
		return font;
	}

	public void setFont(Font font) {
		this.font = font;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}
	
}
